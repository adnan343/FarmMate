import mongoose from 'mongoose';
import Task from '../models/task.model.js';
import { prioritizeTasks, sortByPriority } from '../services/aiPrioritize.js';

const isValidObjectId = (id) => mongoose.Types.ObjectId.isValid(id);

const getAuthorizedFarmerQuery = (req, res) => {
    const farmerId = req.params.farmerId || req.user?._id?.toString();
    if (!farmerId || !isValidObjectId(farmerId)) {
        res.status(400).json({ success: false, message: 'Invalid farmer ID' });
        return null;
    }
    if (req.user.role !== 'admin' && req.user._id.toString() !== farmerId) {
        res.status(403).json({ success: false, message: 'Not authorized to access these tasks' });
        return null;
    }
    return { farmer: farmerId };
};

const validateTaskId = (req, res) => {
    const { id } = req.params;
    if (!id || !isValidObjectId(id)) {
        res.status(400).json({ success: false, message: 'Invalid task ID' });
        return null;
    }
    return id;
};

export const createTask = async (req, res) => {
    try {
        const farmerId = req.user?._id;
        const title = String(req.body?.title || '').trim();
        if (!title) return res.status(400).json({ success: false, message: 'Task title is required' });
        if (title.length > 160) return res.status(400).json({ success: false, message: 'Task title must be 160 characters or less' });

        const payload = {
            ...req.body,
            title,
            farmer: farmerId,
            source: req.body?.source || 'manual',
            autoGenerated: !!req.body?.autoGenerated
        };
        delete payload._id;
        const task = await Task.create(payload);
        res.status(201).json({ success: true, data: task });
    } catch (err) {
        res.status(400).json({ success: false, message: err.message });
    }
};

export const getTasks = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;
        const tasks = await Task.find(query).sort({ dueDate: 1, createdAt: -1 });
        res.json({ success: true, data: tasks });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

export const getTaskById = async (req, res) => {
    try {
        const taskId = validateTaskId(req, res);
        if (!taskId) return;
        const task = await Task.findById(taskId);
        if (!task) return res.status(404).json({ success: false, message: 'Task not found' });

        // H8: Only the task owner or an admin can view a specific task
        if (task.farmer.toString() !== req.user._id.toString() && req.user.role !== 'admin') {
            return res.status(403).json({ success: false, message: 'Not authorized to view this task' });
        }

        res.json({ success: true, data: task });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

export const updateTask = async (req, res) => {
    try {
        const taskId = validateTaskId(req, res);
        if (!taskId) return;
        const task = await Task.findById(taskId);
        if (!task) return res.status(404).json({ success: false, message: 'Task not found' });

        // H8: Only the task owner or an admin can update a task
        if (task.farmer.toString() !== req.user._id.toString() && req.user.role !== 'admin') {
            return res.status(403).json({ success: false, message: 'Not authorized to update this task' });
        }

        const updates = { ...req.body };
        delete updates.farmer;
        delete updates._id;
        if (updates.title !== undefined) {
            updates.title = String(updates.title || '').trim();
            if (!updates.title) return res.status(400).json({ success: false, message: 'Task title is required' });
        }
        const updated = await Task.findByIdAndUpdate(taskId, updates, { new: true, runValidators: true });
        res.json({ success: true, data: updated });
    } catch (err) {
        res.status(400).json({ success: false, message: err.message });
    }
};

export const deleteTask = async (req, res) => {
    try {
        const taskId = validateTaskId(req, res);
        if (!taskId) return;
        const task = await Task.findById(taskId);
        if (!task) return res.status(404).json({ success: false, message: 'Task not found' });

        // H8: Only the task owner or an admin can delete a task
        if (task.farmer.toString() !== req.user._id.toString() && req.user.role !== 'admin') {
            return res.status(403).json({ success: false, message: 'Not authorized to delete this task' });
        }

        await Task.findByIdAndDelete(taskId);
        res.json({ success: true, message: 'Task deleted' });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

export const getSummary = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;

        const now = new Date();
        const [total, completed, pending, overdue] = await Promise.all([
            Task.countDocuments(query),
            Task.countDocuments({ ...query, status: 'completed' }),
            Task.countDocuments({ ...query, status: { $in: ['pending', 'in_progress', 'scheduled'] } }),
            Task.countDocuments({ ...query, status: { $ne: 'completed' }, dueDate: { $lt: now } })
        ]);

        res.json({ success: true, data: { total, completed, pending, overdue } });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

export const getUpcoming = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;
        const now = new Date();
        const soon = new Date();
        soon.setDate(now.getDate() + 14);
        query.dueDate = { $gte: now, $lte: soon };
        query.status = { $ne: 'completed' };
        const tasks = await Task.find(query).sort({ dueDate: 1 });
        res.json({ success: true, data: tasks });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

export const getCategoryProgress = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;
        const match = { farmer: new mongoose.Types.ObjectId(query.farmer) };
        const agg = await Task.aggregate([
            { $match: match },
            { $group: { _id: '$category', total: { $sum: 1 }, completed: { $sum: { $cond: [{ $eq: ['$status', 'completed'] }, 1, 0] } } } },
            { $project: { category: '$_id', _id: 0, total: 1, completed: 1 } }
        ]);
        res.json({ success: true, data: agg });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

/**
 * GET /api/tasks/ai-prioritized/:farmerId
 * Returns tasks sorted by AI score descending
 */
export const getAIPrioritized = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;

        const tasks = await Task.find(query).lean();
        const scored = prioritizeTasks(tasks);
        const sorted = sortByPriority(scored);

        res.json({ success: true, data: sorted });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};

/**
 * GET /api/tasks/grouped/:farmerId
 * Returns tasks grouped into: today, overdue, upcoming, completed
 */
export const getGroupedTasks = async (req, res) => {
    try {
        const query = getAuthorizedFarmerQuery(req, res);
        if (!query) return;

        const now = new Date();
        const startOfDay = new Date(now.getFullYear(), now.getMonth(), now.getDate());
        const endOfDay = new Date(startOfDay.getTime() + 24 * 60 * 60 * 1000);

        const tasks = await Task.find(query).lean();

        // Apply AI prioritization
        const scored = prioritizeTasks(tasks);
        const sorted = sortByPriority(scored);

        const grouped = {
            today: [],
            overdue: [],
            upcoming: [],
            completed: []
        };

        for (const task of sorted) {
            if (task.status === 'completed') {
                grouped.completed.push(task);
                continue;
            }

            if (task.dueDate) {
                const dueDate = new Date(task.dueDate);
                if (dueDate < startOfDay) {
                    grouped.overdue.push(task);
                    continue;
                }
                if (dueDate >= startOfDay && dueDate < endOfDay) {
                    grouped.today.push(task);
                    continue;
                }
                if (dueDate >= endOfDay) {
                    grouped.upcoming.push(task);
                    continue;
                }
            } else {
                // No due date - classify as upcoming if not completed
                if (task.status !== 'completed') {
                    grouped.upcoming.push(task);
                }
            }
        }

        res.json({ success: true, data: grouped });
    } catch (err) {
        res.status(500).json({ success: false, message: err.message });
    }
};


